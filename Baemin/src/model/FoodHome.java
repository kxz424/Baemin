package model;
// 업소 모델
public class FoodHome {
	String fBoss;		// 사업자등록번호
	String fName;		// 업소명
	String fLocation;	// 주소
	String fOpen;		// 오픈시간
	String fClose;		// 마감시간
	String fTel;		// 전화번호
	String fIng;		// 운영중
	String fCategory;	// 카테고리
	String fImg;		// 이미지 경로
	
	public String getfBoss() {
		return fBoss;
	}
	public void setfBoss(String fBoss) {
		this.fBoss = fBoss;
	}
	public String getfName() {
		return fName;
	}
	public void setfName(String fName) {
		this.fName = fName;
	}
	public String getfLocation() {
		return fLocation;
	}
	public void setfLocation(String fLocation) {
		this.fLocation = fLocation;
	}
	public String getfOpen() {
		return fOpen;
	}
	public void setfOpen(String fOpen) {
		this.fOpen = fOpen;
	}
	public String getfClose() {
		return fClose;
	}
	public void setfClose(String fClose) {
		this.fClose = fClose;
	}
	public String getfTel() {
		return fTel;
	}
	public void setfTel(String fTel) {
		this.fTel = fTel;
	}
	public String getfIng() {
		return fIng;
	}
	public void setfIng(String fIng) {
		this.fIng = fIng;
	}
	public String getfCategory() {
		return fCategory;
	}
	public void setfCategory(String fCategory) {
		this.fCategory = fCategory;
	}
	public String getfImg() {
		return fImg;
	}
	public void setfImg(String fImg) {
		this.fImg = fImg;
	}
	
}
