package model;
// 사장님 모델
public class Boss {
	String bNum; 	  // 사업자등록번호
	String bId; 	  // 아이디
	String bPassword; // 비밀번호
	String bName;     // 이름
	String bMail;	  // 이메일
	String bBirth;	  // 생년월일
	String bTel;	  // 전화번호
	
	public String getbNum() {
		return bNum;
	}
	public void setbNum(String bNum) {
		this.bNum = bNum;
	}
	public String getbId() {
		return bId;
	}
	public void setbId(String bId) {
		this.bId = bId;
	}
	public String getbPassword() {
		return bPassword;
	}
	public void setbPassword(String bPassword) {
		this.bPassword = bPassword;
	}
	public String getbName() {
		return bName;
	}
	public void setbName(String bName) {
		this.bName = bName;
	}
	public String getbMail() {
		return bMail;
	}
	public void setbMail(String bMail) {
		this.bMail = bMail;
	}
	public String getbBirth() {
		return bBirth;
	}
	public void setbBirth(String bBirth) {
		this.bBirth = bBirth;
	}
	public String getbTel() {
		return bTel;
	}
	public void setbTel(String bTel) {
		this.bTel = bTel;
	}
	
}
